#include <stdio.h>

int main(void){

	printf("Size of char on this computer is %lu \n",sizeof(char)); // %lu is unsigned long
	printf("Size of int on this computer is %zu \n",sizeof(int));   // %zu is return type of sizeof
	printf("Size of double on this computer is %zu \n",sizeof(double));
	printf("Size of float on this computer is %zu \n",sizeof(float));
	printf("Size of long and unsigned  on this computer is %zu \n",sizeof(unsigned long));
	
	

}		
